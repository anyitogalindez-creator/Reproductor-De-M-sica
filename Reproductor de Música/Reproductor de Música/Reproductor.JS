// Arreglos de control de estado
let favorites = [];
let currentSongIndex = 0;
let isShuffleActive = false; // Control de Modo Aleatorio
const audio = new Audio();

// Capturas del DOM
const songTitle = document.getElementById("song-title");
const songArtist = document.getElementById("song-artist");
const songCover = document.getElementById("song-cover");
const vinylWrapper = document.getElementById("song-cover-container");

const btnPlayPause = document.querySelector("#btn-play-pause");
const iconPlayPause = btnPlayPause.querySelector(".material-symbols-outlined");
const btnNext = document.querySelector("#btn-next");
const btnPrev = document.querySelector("#btn-prev");
const btnFavorite = document.getElementById("btn-favorite");
const iconFavorite = document.getElementById("icon-favorite");
const btnShuffle = document.getElementById("btn-shuffle"); // Captura de Shuffle

const progressBar = document.querySelector(".progress-bar");
const currentTimeLabel = document.querySelector(".current-time");
const totalTimeLabel = document.querySelector(".total-time");
const volumeSlider = document.querySelector(".volume-slider");

const menuLinks = document.querySelectorAll(".menu-links a");
const views = document.querySelectorAll(".music-view");

const searchInputField = document.getElementById("search-input-field");
const searchResultsContainer = document.getElementById("search-results");
const libraryResultsContainer = document.getElementById("library-results");
const libraryEmptyMsg = document.getElementById("library-empty-msg");

// Carga de pista
function loadSong(song) {
    audio.src = song.src;
    songTitle.textContent = song.title;
    songArtist.textContent = song.artist;
    songCover.src = song.cover;
    
    const lyricsElement = document.getElementById("song-lyrics");
    if (lyricsElement) {
        lyricsElement.textContent = song.lyrics || "Esta canción no tiene letra disponible.";
    }

    progressBar.value = 0;
    currentTimeLabel.textContent = "0:00";
    updateFavoriteButtonState(); 
}

function playSong() {
    audio.play();
    iconPlayPause.textContent = "pause";
    btnPlayPause.setAttribute("title", "Pausar");
    vinylWrapper.classList.add("playing");
}

function pauseSong() {
    audio.pause();
    iconPlayPause.textContent = "play_arrow";
    btnPlayPause.setAttribute("title", "Reproducir");
    vinylWrapper.classList.remove("playing");
}

function togglePlay() {
    if (audio.paused) {
        playSong();
    } else {
        pauseSong();
    }
}

// Lógica adaptada para siguiente/anterior con soporte de Shuffle integrado
function nextSong() {
    if (isShuffleActive && playlist.length > 1) {
        let newIndex;
        do {
            newIndex = Math.floor(Math.random() * playlist.length);
        } while (newIndex === currentSongIndex);
        currentSongIndex = newIndex;
    } else {
        currentSongIndex = (currentSongIndex + 1) % playlist.length;
    }
    loadSong(playlist[currentSongIndex]);
    playSong();
}

function prevSong() {
    if (isShuffleActive && playlist.length > 1) {
        let newIndex;
        do {
            newIndex = Math.floor(Math.random() * playlist.length);
        } while (newIndex === currentSongIndex);
        currentSongIndex = newIndex;
    } else {
        currentSongIndex = (currentSongIndex - 1 + playlist.length) % playlist.length;
    }
    loadSong(playlist[currentSongIndex]);
    playSong();
}

function toggleShuffle() {
    isShuffleActive = !isShuffleActive;
    if (isShuffleActive) {
        btnShuffle.classList.add("active-shuffle");
        btnShuffle.setAttribute("title", "Desactivar Aleatorio");
    } else {
        btnShuffle.classList.remove("active-shuffle");
        btnShuffle.setAttribute("title", "Aleatorio");
    }
}

function formatTime(seconds) {
    if (isNaN(seconds)) return "0:00";
    const mins = Math.floor(seconds / 60);
    const secs = Math.floor(seconds % 60);
    return `${mins}:${secs < 10 ? '0' : ''}${secs}`;
}

// Búsqueda y Biblioteca
function renderSongsList(songsArray, container) {
    container.innerHTML = "";
    if (songsArray.length === 0) return;

    songsArray.forEach((song) => {
        const originalIndex = playlist.findIndex(p => p.title === song.title);

        const songItem = document.createElement("div");
        songItem.classList.add("list-song-item");
        songItem.innerHTML = `
            <div class="song-item-info">
                <img src="${song.cover}" class="list-song-cover" alt="Cover">
                <div class="list-song-details">
                    <h4>${song.title}</h4>
                    <p>${song.artist}</p>
                </div>
            </div>
            <button class="btn-play-item" data-index="${originalIndex}">
                <span class="material-symbols-outlined">play_arrow</span>
            </button>
        `;
        container.appendChild(songItem);
    });

    container.querySelectorAll(".btn-play-item").forEach(button => {
        button.addEventListener("click", () => {
            const indexToPlay = parseInt(button.getAttribute("data-index"));
            currentSongIndex = indexToPlay;
            loadSong(playlist[currentSongIndex]);
            playSong();
            switchView(0);
        });
    });
}

searchInputField.addEventListener("input", () => {
    const query = searchInputField.value.toLowerCase().trim();
    if (query === "") {
        searchResultsContainer.innerHTML = "";
        return;
    }
    const filteredSongs = playlist.filter(song => 
        song.title.toLowerCase().includes(query) || 
        song.artist.toLowerCase().includes(query)
    );
    renderSongsList(filteredSongs, searchResultsContainer);
});

function toggleFavorite() {
    const currentSong = playlist[currentSongIndex];
    const isFavorite = favorites.some(fav => fav.title === currentSong.title);

    if (isFavorite) {
        favorites = favorites.filter(fav => fav.title !== currentSong.title);
    } else {
        favorites.push(currentSong);
    }
    updateFavoriteButtonState();
    updateLibraryView();
}

function updateFavoriteButtonState() {
    const currentSong = playlist[currentSongIndex];
    const isFavorite = favorites.some(fav => fav.title === currentSong.title);

    if (isFavorite) {
        iconFavorite.textContent = "favorite";
        btnFavorite.classList.add("active-fav");
    } else {
        iconFavorite.textContent = "favorite_border";
        btnFavorite.classList.remove("active-fav");
    }
}

function updateLibraryView() {
    if (favorites.length === 0) {
        libraryEmptyMsg.style.display = "block";
        libraryResultsContainer.innerHTML = "";
    } else {
        libraryEmptyMsg.style.display = "none";
        renderSongsList(favorites, libraryResultsContainer);
    }
}

function switchView(activeIndex) {
    menuLinks.forEach(item => item.classList.remove("active"));
    views.forEach(view => view.classList.remove("active-view"));

    menuLinks[activeIndex].classList.add("active");
    
    if (activeIndex === 0) {
        document.getElementById("view-home").classList.add("active-view");
    } else if (activeIndex === 1) {
        document.getElementById("view-search").classList.add("active-view");
    } else if (activeIndex === 2) {
        document.getElementById("view-library").classList.add("active-view");
        updateLibraryView(); 
    }
}

// Inicialización
loadSong(playlist[currentSongIndex]);
audio.volume = volumeSlider.value / 100;

// Listeners
btnPlayPause.addEventListener("click", togglePlay);
btnNext.addEventListener("click", nextSong);
btnPrev.addEventListener("click", prevSong);
btnFavorite.addEventListener("click", toggleFavorite);
btnShuffle.addEventListener("click", toggleShuffle); // Listener de Shuffle añadido

audio.addEventListener("timeupdate", () => {
    if (!audio.duration) return;
    const progressPercent = (audio.currentTime / audio.duration) * 100;
    progressBar.value = progressPercent;
    currentTimeLabel.textContent = formatTime(audio.currentTime);
});

audio.addEventListener("loadedmetadata", () => {
    totalTimeLabel.textContent = formatTime(audio.duration);
});

// Doble listener (input y change) para máxima estabilidad de sincronización
progressBar.addEventListener("input", () => {
    const newTime = (progressBar.value / 100) * audio.duration;
    currentTimeLabel.textContent = formatTime(newTime);
});

progressBar.addEventListener("change", () => {
    const newTime = (progressBar.value / 100) * audio.duration;
    audio.currentTime = newTime;
});

volumeSlider.addEventListener("input", () => {
    audio.volume = volumeSlider.value / 100;
});

audio.addEventListener("ended", nextSong);

menuLinks.forEach((link, index) => {
    link.addEventListener("click", (event) => {
        event.preventDefault();
        switchView(index);
    });
});

function updateSongsCounter() {
    const counterElement = document.getElementById("total-songs-counter");
    if (counterElement) {
        counterElement.textContent = `Total de canciones disponibles: ${playlist.length}`;
    }
}

updateSongsCounter();